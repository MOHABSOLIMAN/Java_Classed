package education_institute;

public class Subject {
	private String name;
	private int Grade;
	
	public String getName() {
	    return name;
	  }

	public void setName(String newName) {
	    this.name = newName;
	  }
	
	public int getGrade() {
	    return Grade;
	  }

	public void setGrade(int newGrade) {
	    this.Grade = newGrade;
	  }
}
